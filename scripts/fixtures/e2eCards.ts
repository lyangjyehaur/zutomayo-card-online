const ELEMENT_SLUGS: Record<Exclude<Element, 'カオス'>, string> = {
  闇: 'dark',
  炎: 'flame',
  電気: 'electric',
  風: 'wind',
};

function syntheticCard(element: Exclude<Element, 'カオス'>, type: CardDef['type'], index: number): CardDef {
  const slug = ELEMENT_SLUGS[element];
  const typeSlug = type === 'Character' ? 'character' : type === 'Enchant' ? 'enchant' : 'area-enchant';
  return {
    id: `e2e-${slug}-${typeSlug}-${String(index).padStart(2, '0')}`,
    name: `E2E ${slug} ${typeSlug} ${index}`,
    pack: 'E2E',
    song: 'Synthetic Fixture',
    illustrator: 'E2E',
    rarity: 'N',
    element,
    type,
    clock: (index % 3) + 1,
    attack: type === 'Character' ? { night: 20 + index, day: 25 + index } : null,
    powerCost: index % 3,
    sendToPower: type === 'Character' ? 1 : 0,
    effect: '',
    image: '/card-back.jpg',
    errata: '',
  };
}

function cardsForElement(element: Exclude<Element, 'カオス'>): CardDef[] {
  const cards = TUTORIAL_CARDS.filter((card) => card.element === element);
  const characterCount = cards.filter((card) => card.type === 'Character').length;
  const nonCharacterCount = cards.length - characterCount;

  for (let index = characterCount + 1; index <= 15; index += 1) {
    cards.push(syntheticCard(element, 'Character', index));
  }
  for (let index = nonCharacterCount + 1; index <= 10; index += 1) {
    cards.push(syntheticCard(element, index % 2 === 0 ? 'Area Enchant' : 'Enchant', index));
  }
  return cards;
}

